    public void TodasLasCancionesUsuario()
    {
        a_a.cargarArchivo();
        DefaultListModel modelo=(DefaultListModel)lista_canciones.getModel();
        modelo.clear();
        for (int i = 0; i <a_a.getArtistas().size(); i++) {
            for (int j = 0; j <a_a.getArtistas().get(i).getCanciones().size(); j++) {
                modelo.addElement(a_a.getArtistas().get(i).getCanciones().get(j));
                lista_canciones.setModel(modelo);
                todas.add(a_a.getArtistas().get(i).getCanciones().get(j));
            }
        }
        a_a.cargarArchivo();
        for (int i = 0; i <a_a.getArtistas().size(); i++) {
            for (int j = 0; j <a_a.getArtistas().get(i).getSencillos().size(); j++) {
                modelo.addElement(a_a.getArtistas().get(i).getSencillos().get(j));
                lista_canciones.setModel(modelo);
                todas.add(a_a.getArtistas().get(i).getSencillos().get(j));
            }
        }
    }
