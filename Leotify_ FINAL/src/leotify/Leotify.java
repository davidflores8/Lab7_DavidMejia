    public void TodasLasCanciones()
    {
        a_a.cargarArchivo();
        DefaultListModel modelo=(DefaultListModel)lista_todas_las_canciones.getModel();
        modelo.clear();
        for (int i = 0; i <a_a.getArtistas().size(); i++) {
            for (int j = 0; j <a_a.getArtistas().get(i).getCanciones().size(); j++) {
                modelo.addElement(a_a.getArtistas().get(i).getCanciones().get(j));
                lista_todas_las_canciones.setModel(modelo);
            }
        }
        a_a.cargarArchivo();
        for (int i = 0; i <a_a.getArtistas().size(); i++) {
            for (int j = 0; j <a_a.getArtistas().get(i).getSencillos().size(); j++) {
                modelo.addElement(a_a.getArtistas().get(i).getSencillos().get(j));
                lista_todas_las_canciones.setModel(modelo);
            }
        }
    }
    
